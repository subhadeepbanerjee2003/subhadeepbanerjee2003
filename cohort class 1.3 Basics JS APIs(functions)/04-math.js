function mathMethods(value) {
  console.log("original value:", value);

  // Math.round()
  console.log("Math.round():", Math.round(value));

  // Math.ceil()
  console.log("Math.ceil():", Math.ceil(value));

  // Math.floor()
  console.log("Math.floor():", Math.floor(value));

  // Math.random()
  console.log("Math.random():", Math.random());

  // Math.max()
  console.log("Math.max():", Math.max(1, 2, 3, 4, 5));

  // Math.min()
  console.log("Math.min():", Math.min(1, 2, 3, 4, 5));

  // Math.pow()
  console.log("Math.pow():", Math.pow(2, 3));

  // Math.sqrt()
  console.log("Math.sqrt():", Math.sqrt(9));

  // Math.abs()
  console.log("Math.abs():", Math.abs(-1));

  // Math.PI
  console.log("Math.PI:", Math.PI);

  // Math.E
  console.log("Math.E:", Math.E);
}

//example usage of math methods
mathMethods(4.56);
mathMethods(9);
mathMethods(25);

// ceil() function gives the samllest integer bigger than the given value
// floor() function gives the largest integer smaller than the given value
